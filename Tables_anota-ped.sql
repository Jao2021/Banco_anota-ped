Create Table Pessoa (
	ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	NOME VARCHAR(20) NOT NULL,
	PRIMARY KEY (ID)	
)

CREATE TABLE Funcionario (
	PESSOA_ID INTEGER NOT NULL REFERENCES Pessoa(ID),
	EMAIL VARCHAR(30),
	SENHA VARCHAR(20),
	PRIMARY KEY (PESSOA_ID)
)

CREATE TABLE Cliente (
	PESSOA_ID INTEGER NOT NULL REFERENCES Pessoa(ID),
	TELEFONE VARCHAR(9),
	PRIMARY KEY (PESSOA_ID)
)

CREATE TABLE Pedido(
	ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	CLIENTE_ID INTEGER NOT NULL REFERENCES Cliente (PESSOA_ID),
	FUNCIONARIO_ID INTEGER NOT NULL REFERENCES Funcionario (PESSOA_ID),
	STATUS INTEGER,
	DATA DATE,
	VALOR_TOTAL MONEY,
	PRIMARY KEY (ID)
)

CREATE TABLE Produtos (
	ID INTEGER NOT NULL GENERATED BY DEFAULT AS IDENTITY,
	PRECO MONEY,
	NOME VARCHAR(20),
	ESTOQUE INTEGER,
	PRIMARY KEY (ID)
)

CREATE TABLE Ped_prod(
	PEDIDO_ID INTEGER NOT NULL REFERENCES Pedido(ID),
	PRODUTO_ID INTEGER NOT NULL REFERENCES Produtos(ID),
	QTD INTEGER,
	VALOR_UNIT MONEY,
	PRIMARY KEY (PEDIDO_ID, PRODUTO_ID)
)